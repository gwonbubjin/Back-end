package com.ratego.model;

import java.util.Date;
import lombok.Data;

/*******************************************************************************
 * [PointVO]
 * 설명 : 포인트 변동 내역(로그)을 담는 객체
 * 매핑 테이블 : tbl_point (예상)
 * 용도 : 마이페이지 포인트 히스토리 및 관리자 조회용
 * 프로젝트 : RateGo Project
 *******************************************************************************/
@Data
public class PointVO {

    /* =================================================================
     * 1. 식별 데이터 (PK & FK)
     * ================================================================= */
    private int pno;           // 포인트 로그 고유 번호 (PK, Auto Increment)
    private String userId;     // 해당 포인트의 주인 ID (Member 테이블 참조)


    /* =================================================================
     * 2. 거래(변동) 핵심 정보
     * ================================================================= */
    private int amount;        // 변동 액수 (양수(+) : 획득 / 음수(-) : 사용)
    private String reason;     // 변동 사유 (예: "출석체크", "게시글 작성", "아이템 구매")


    /* =================================================================
     * 3. 메타 데이터
     * ================================================================= */
    private Date regdate;      // 변동 발생 일시 (기록 시간)

}