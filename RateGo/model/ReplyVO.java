package com.ratego.model;

import java.util.Date;
import lombok.Data;

/*******************************************************************************
 * [ReplyVO]
 * 설명 : 댓글(Reply) 데이터를 담는 객체
 * 매핑 테이블 : tbl_reply
 * 특이사항 : 대댓글 기능(parent_rno) 및 마이페이지 표시용 필드(boardTitle) 포함
 * 프로젝트 : RateGo Project
 *******************************************************************************/
@Data
public class ReplyVO {

    /* =================================================================
     * 1. 식별자 및 구조 (Key & Hierarchy)
     * ================================================================= */
    private int rno;           // 댓글 고유 번호 (PK)
    private int bno;           // 소속 게시글 번호 (FK)
    
    // ★ 대댓글(Nested Reply) 구현을 위한 부모 댓글 번호 (0 or null이면 최상위)
    private int parent_rno;    


    /* =================================================================
     * 2. 댓글 본문 및 작성자 정보
     * ================================================================= */
    private String reply;      // 댓글 내용
    
    private String replyer;    // 작성자 ID (DB 저장용)
    private String replyerName;// 작성자 닉네임 (화면 표시용, 조인해서 가져옴)
    
    private Date replyDate;    // 작성일


    /* =================================================================
     * 3. 확장 필드 (DB 테이블에는 없지만 화면에 필요해서 추가)
     * ================================================================= */
    
    // [마이페이지 - 내 댓글 목록]에서 "어떤 글에 썼는지" 제목을 보여주기 위함
    private String boardTitle; 

}