package com.ratego.model;

import java.util.Date; // ★ 중요: MyBatis 구버전 호환성 위해 java.time 대신 사용
import lombok.Data; 

/*******************************************************************************
 * [BoardVO]
 * 설명 : 게시글 데이터를 담는 객체 (Value Object)
 * 매핑 테이블 : tbl_board
 * 프로젝트 : RateGo Project
 *******************************************************************************/
@Data
public class BoardVO {

    /* =================================================================
     * 1. 게시글 핵심 정보 (PK, 제목, 내용, 작성자)
     * ================================================================= */
    private Long bno;          // 게시글 고유 번호 (PK)
    private String title;      // 제목
    private String content;    // 내용
    private String writer;     // 작성자 닉네임 (화면 표시용)
    private String writerId;   // 작성자 ID (DB 조인 및 식별용)
    
    // ★ 추가된 필드: 카테고리 (자유/질문/정보 등 구분)
    private String category; 


    /* =================================================================
     * 2. 메타 데이터 (시간 정보)
     * ================================================================= */
    private Date regdate;      // 작성일
    private Date updatedate;   // 수정일


    /* =================================================================
     * 3. 통계 데이터 (조회수, 댓글수)
     * ================================================================= */
    private int viewcnt;       // 조회수 (default 0)
    private int replycnt;      // 댓글 수 (반정규화: join 최소화 목적)


    /* =================================================================
     * 4. 상호작용 데이터 (좋아요/싫어요)
     * ================================================================= */
    private int likecnt;       // 좋아요 수
    private int dislikecnt;    // 싫어요 수

}