package com.ratego.model;

import java.util.List;

/*******************************************************************************
 * [ReplyMapper Interface]
 * 설명 : 댓글 테이블(tbl_reply)과 관련된 SQL을 호출하는 인터페이스
 * 프로젝트 : RateGo Project
 *******************************************************************************/
public interface ReplyMapper {

    /*******************************************************************************
     * 1. [CREATE] 댓글 등록
     *******************************************************************************/
    public int insert(ReplyVO vo);


    /*******************************************************************************
     * 2. [READ] 댓글 조회
     * - getList : 특정 게시글(bno)에 달린 모든 댓글 가져오기
     * - read    : 특정 댓글(rno) 하나만 읽기 (수정 전 데이터 로드용)
     *******************************************************************************/
    public List<ReplyVO> getList(Long bno);
    
    public ReplyVO read(int rno); 


    /*******************************************************************************
     * 3. [UPDATE & DELETE] 댓글 수정 및 삭제
     * - 반환타입 int : 성공한 행의 개수 (1이면 성공)
     *******************************************************************************/
    public int delete(int rno);
    
    public int update(ReplyVO vo);

}