package com.ratego.service;

import java.util.List;
import com.ratego.model.ReplyVO;

/*******************************************************************************
 * [ReplyService Interface]
 * 설명 : 댓글(Reply) 관련 비즈니스 로직을 정의하는 인터페이스
 * 역할 : Controller와 Mapper 사이의 연결 고리
 * 프로젝트 : RateGo Project
 *******************************************************************************/
public interface ReplyService {
    
    /*******************************************************************************
     * 1. [CREATE] 댓글 등록
     * - 반환값 int : 성공 시 1 반환 (Mapper의 결과 그대로 전달)
     *******************************************************************************/
    public int register(ReplyVO vo);


    /*******************************************************************************
     * 2. [READ] 댓글 목록 조회
     * - 특정 게시글(bno)에 달린 댓글 리스트 반환
     *******************************************************************************/
    public List<ReplyVO> getList(Long bno);
    

    /*******************************************************************************
     * 3. [UPDATE & DELETE] 댓글 수정 및 삭제 (★ 추가됨)
     * - remove : 댓글 번호(rno)로 삭제
     * - modify : 댓글 내용 수정
     * - 반환값 int : 성공 여부 (1: 성공, 0: 실패)
     *******************************************************************************/
    
    // 댓글 삭제
    public int remove(int rno);
    
    // 댓글 수정
    public int modify(ReplyVO vo);

}